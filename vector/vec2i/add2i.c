#include "mathex/vector.h"

v2i					v2iaddv2i(v2i a, v2i b)
{
	return ((v2i){ a.x + b.x, a.y + b.y });
}

v2i					*pv2iaddv2i(v2i *a, const v2i b)
{
	a->x += b.x;
	a->y += b.y;
	return (a);
}
