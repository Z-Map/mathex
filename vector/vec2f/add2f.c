#include "mathex/vector.h"

v2f					v2faddv2f(v2f a, v2f b)
{
	return ((v2f){ a.x + b.x, a.y + b.y });
}

v2f					*pv2faddv2f(v2f *a, const v2f b)
{
	a->x += b.x;
	a->y += b.y;
	return (a);
}
